package data;

public class Point{
    
	
	
	Integer x;    
	Integer y;
	
    public Point(int i, int j) {
		this.x=i;
		this.y=j;
	}
    
	public int getX() {
        return x;
    }
    void setX(int x) {
        this.x = x;
    }
    public int getY() {
        return y;
    }
    void setY(int y) {
        this.y = y;
    }
    
    @Override
    public String toString() {
    	return this.toString();
    }
}
